        <h2>What is a repository:</h2>

        
        <p>A repository is how you can organize your project. A repository is like a folder that contains all your project files and the stores the history or each revision. A repository can contain anything needed for your project. This could include files, images, spreadsheets, data sets, etc. Repositories can either be public or private. The project owner can give others access to the project so that they can contribute to the repositions, making that individual a project collaborator. Everyone can view a private repository since they’re public, but only collaborators can contribute to a private repository. It is very helpful to comment your repository. This usually means that along will well documented code, it’s important to include a “README” file that contains information about the project within each repository. </p>
        <h3>How to make a repository:</h3>
        <p>
